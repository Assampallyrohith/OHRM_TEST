package DecisionMakingStatements;

public class NestedloopsExample1 
{
public static void main(String[]args)
{

	for(int i=3;i<=4;i++)
	{
		for(int j=1;j<=5;j++)
		{
			System.out.println("rohith");
		}
		System.out.println("naveen");
	}
	System.out.println("======================");
		for(int i=5;i>=-1;i--)
		{
			for(int j=1;j>=-5;j--)
			{
				System.out.println(j+""+"jarvis");
			}
		 System.out.println(i+"ironman");
		}
		System.out.println("======================");
	for(byte z=0;z<=10;z++) 
	{
		for(long y=1;y>=-5;y--)
		{
		System.out.println(y+"hello");	
		}
	System.out.println(z+"bye");
	}
		System.out.println("======================");
	
	
	
}
}
